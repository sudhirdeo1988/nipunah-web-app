export function normalizeRoleKey(roleKey) {
  const key = String(roleKey || "").toLowerCase();
  if (key && ROLE_MANAGEMENT_MOCK[key]) return key;
  return "admin";
}

export function getRolePermissionObject(roleKey) {
  return ROLE_MANAGEMENT_MOCK[normalizeRoleKey(roleKey)];
}
